function buildEntries(items) {
  return items.map(([target, japanese, explanation]) => ({
    english: target,
    japanese,
    explanation
  }));
}

function dedupeEntries(items) {
  const seen = new Set();

  return items.filter(({ english, japanese }) => {
    const key = `${english.trim().toLowerCase()}::${japanese.trim()}`;
    if (seen.has(key)) {
      return false;
    }

    seen.add(key);
    return true;
  });
}

function createCollection(groups, kind) {
  return Object.fromEntries(Object.entries(categoryMeta).map(([key, meta]) => [
    key,
    {
      label: meta.label,
      description: kind === "word" ? meta.wordDescription : meta.phraseDescription,
      words: dedupeEntries(buildEntries(groups[key] || []))
    }
  ]));
}

function mergeGroups(base, addition) {
  return Object.fromEntries(Object.keys(categoryMeta).map((key) => [
    key,
    [...(base[key] || []), ...(addition[key] || [])]
  ]));
}

function targetCountsFrom(collection) {
  return Object.fromEntries(Object.entries(collection).map(([key, category]) => [
    key,
    category.words.length
  ]));
}

